const touchStart = (e) => {
  isDrawing = true
  hasSignature.value = true
  startX = e.changedTouches[0].x
  startY = e.changedTouches[0].y
  ctx.beginPath()
  ctx.moveTo(startX, startY)
}

const touchMove = (e) => {
  if (!isDrawing) return
  
  const currentX = e.changedTouches[0].x
  const currentY = e.changedTouches[0].y
  
  ctx.lineTo(currentX, currentY)
  ctx.stroke()
  ctx.draw(true)
  
  startX = currentX
  startY = currentY
}

const touchEnd = () => {
  isDrawing = false
}

